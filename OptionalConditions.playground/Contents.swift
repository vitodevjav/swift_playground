import UIKit

var bool: Bool? = false

if bool != false {
    print("false")
} else {
    print(bool)
}

if bool != true {
    print("true")
} else {
    print(bool)
}

bool = true

if bool != false {
    print("false")
} else {
    print(bool)
}

if bool != true {
    print("true")
} else {
    print(bool)
}

bool = nil

if bool == false {
    print("false")
} else {
    print(bool)
}

if bool == true {
    print("true")
} else {
    print(bool)
}
